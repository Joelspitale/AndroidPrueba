package profile.recyclerView;
public class Profile {
    private String name;
    private String hexa;

    public Profile(String name, String hexa) {
        this.name = name;
        this.hexa = hexa;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String gethexa() {
        return hexa;
    }

    public void setApellido(String apellido) {
        this.hexa = hexa;
    }
}
